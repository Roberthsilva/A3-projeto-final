    private void cadastarTableContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastarTableContaActionPerformed
        String contas = txtconta.getText();
        String preco = txtpreco.getText();
        
        contaModel sistemaCerto = new contaModel(contas,preco);
        
        contaRepository repository = new contaRepository();
        
        repository.CreateContas(sistemaCerto);
        
       txtconta.setText("");
       txtpreco.setText("");
    }//GEN-LAST:event_cadastarTableContaActionPerformed

    private void excluirTableContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirTableContaActionPerformed
        
        if(tableconta.getSelectedRow() != -1){
        
DefaultTableModel dtmProdutos = (DefaultTableModel) tableconta.getModel();
dtmProdutos.removeRow(tableconta.getSelectedRow());
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto pra excluir.");
        }
        
        
    }//GEN-LAST:event_excluirTableContaActionPerformed

