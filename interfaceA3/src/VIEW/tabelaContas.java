    private void btnDadosContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDadosContaActionPerformed
 
        try {
            contaRepository contarepositorio = new contaRepository();
           List<contaModel> listaconta = contarepositorio.readAllConta();
            DefaultTableModel tableConta
                    = (DefaultTableModel) tableconta.getModel();
            tableConta.setNumRows(0);
            for (contaModel ContaModel : listaconta) {
                Object[] novoProduto = new Object[]{
                    ContaModel.getConta(), ContaModel.getPreco()
                };
                tableConta.addRow(novoProduto);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro");
        }


        
    }//GEN-LAST:event_btnDadosContaActionPerformed


